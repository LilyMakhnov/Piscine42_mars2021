#include <unistd.h>

void	ft_print_comb(void)
{
	char tab[5];

	tab[0] = '0';
	tab[1] = '1';
	tab[2] = '2';
	tab[3] = ',';
	tab[4] = ' ';
	while (tab[0] < '8')
	{
		write(1, tab, (tab[0] == '7' ? 3 : 5));
		if (++tab[2] > '9')
		{
			if (++tab[1] > '8')
			{
				tab[1] = ++tab[0] + 1;
			}
			tab[2] = tab[1] + 1;
		}
	}
}
