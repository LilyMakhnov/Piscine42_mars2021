int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int i;

	i = 0;
	if (n == 0)
		return (0);
	while (*s1 && *s2 && *s1 == *s2 && i + 1 < n)
	{
		s1++;
		s2++;
		i++;
	}
	return (*s1-*s2);
}
