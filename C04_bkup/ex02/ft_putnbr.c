#include <unistd.h>

void	ft_putnbr(int nb)
{
	char to_print;

	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else
	{
		if (nb < 0)
		{
			nb = -nb;
			write(1, "-", 1);
		}
		while (nb > 9)
		{
			ft_putnbr(nb / 10);
			nb %= 10;
		}
		to_print = nb + '0';
		write(1, &to_print, 1);
	}
}
